#!/usr/bin/env node
/**
 * Writes google-maps-config.js from GOOGLE_MAPS_API_KEY.
 * - Local: set the key in .env (see .env.example), then run: npm run build
 * - Vercel: add GOOGLE_MAPS_API_KEY in Project → Settings → Environment Variables
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const envPath = path.join(root, ".env");

function loadDotEnv() {
  if (!fs.existsSync(envPath)) return;
  const text = fs.readFileSync(envPath, "utf8");
  for (const line of text.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const k = trimmed.slice(0, eq).trim();
    let v = trimmed.slice(eq + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    if (k && process.env[k] === undefined) process.env[k] = v;
  }
}

loadDotEnv();

const key = (process.env.GOOGLE_MAPS_API_KEY || "").trim();
const outPath = path.join(root, "google-maps-config.js");

const content = `/* Generated by npm run build — do not edit by hand. */
window.__GOOGLE_MAPS_API_KEY__ = ${JSON.stringify(key)};
`;

fs.writeFileSync(outPath, content, "utf8");
if (key) {
  console.log("google-maps-config.js written (Maps API key present).");
} else {
  const onVercel = process.env.VERCEL === "1";
  console.warn(
    onVercel
      ? "GOOGLE_MAPS_API_KEY was empty during this Vercel build — distances will not load until you set it for Production (and Preview) and redeploy."
      : "GOOGLE_MAPS_API_KEY is empty. Add it to .env locally or to Vercel env vars, then run npm run build again."
  );
}
