#!/usr/bin/env node
/**
 * Writes google-maps-config.js from env:
 * - GOOGLE_MAPS_API_KEY — browser Maps JS + Directions + Places (referrer-restricted key).
 * - GOOGLE_MAPS_SERVER_KEY — optional; Weather proxy (api/weather.js) prefers this server key.
 * - GOOGLE_CLIENT_ID — optional OAuth web client id as window.__GOOGLE_OAUTH_CLIENT_ID__
 *   (public in the bundle). Never put GOOGLE_CLIENT_SECRET in generated JS.
 *
 * Weather uses api/weather.js with GOOGLE_MAPS_SERVER_KEY if set, else GOOGLE_MAPS_API_KEY
 * (HTTP-referrer keys often fail from Node — use a separate server key; see .env.example).
 *
 * Vercel: expose GOOGLE_MAPS_API_KEY (and GOOGLE_CLIENT_ID if you use it) to the Build step.
 * google-maps-config.js is gitignored; npm run build must run on deploy.
 */
const fs = require("fs");
const path = require("path");

const root = path.join(__dirname, "..");
const envPath = path.join(root, ".env");

function loadDotEnv() {
  if (!fs.existsSync(envPath)) return;
  const text = fs.readFileSync(envPath, "utf8");
  for (const line of text.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const k = trimmed.slice(0, eq).trim();
    let v = trimmed.slice(eq + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    if (k && process.env[k] === undefined) process.env[k] = v;
  }
}

loadDotEnv();

const key = (process.env.GOOGLE_MAPS_API_KEY || "").trim();
const oauthClientId = (process.env.GOOGLE_CLIENT_ID || "").trim();
const outPath = path.join(root, "google-maps-config.js");

const content = `/* Generated by npm run build — do not edit by hand. */
window.__GOOGLE_MAPS_API_KEY__ = ${JSON.stringify(key)};
window.__GOOGLE_OAUTH_CLIENT_ID__ = ${JSON.stringify(oauthClientId)};
`;

fs.writeFileSync(outPath, content, "utf8");
if (key) {
  console.log(
    oauthClientId
      ? "google-maps-config.js written (Maps API key + OAuth client id present)."
      : "google-maps-config.js written (Maps API key present)."
  );
} else {
  const onVercel = process.env.VERCEL === "1";
  console.warn(
    onVercel
      ? "GOOGLE_MAPS_API_KEY was empty during this Vercel build — distances will not load until you set it for Production (and Preview) and redeploy."
      : "GOOGLE_MAPS_API_KEY is empty. Add it to .env locally or to Vercel env vars, then run npm run build again."
  );
}
